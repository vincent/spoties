    </p>
    <ul class="mb-2 list-inside list-disc text-gray-700 dark:text-gray-300">
      <li>«&nbsp;Sécurité à l'entrée principale&nbsp;»</li>
      <li>«&nbsp;Coordination des coulisses&nbsp;»</li>
      <li>«&nbsp;Gestion de l'espace VIP&nbsp;»</li>
