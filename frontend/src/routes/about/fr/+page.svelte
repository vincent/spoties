        Fournissez des détails complets sur votre événement, notamment&nbsp;:
        <ul class="ml-6 list-inside list-disc">
          <li>Date et durée</li>
          <li>Objectif général et buts</li>
          <li>Ce à quoi les participants peuvent s'attendre</li>
          <li>Toutes exigences ou préparations particulières</li>
        </ul>
      </li>
    </ul>
    <p class="mb-6 text-gray-700 dark:text-gray-300">
