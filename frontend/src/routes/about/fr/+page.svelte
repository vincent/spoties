      <li>Conférences d'entreprise avec des salles de sous-commission</li>
      <li>Événements communautaires couvrant différents quartiers</li>
      <li>Salons professionnels avec diverses zones d'exposition</li>
      <li>Tournois sportifs dans plusieurs installations</li>
    </ul>
    <p class="mb-6 text-gray-700 dark:text-gray-300">
      Le principal défi est de garantir que chaque site dispose d'un personnel
      suffisant tout en maintenant une communication claire entre tous les
      lieux.
