    <h4 class="mt-6 mb-2 text-lg font-semibold text-gray-900 dark:text-white">
      Créer des questions efficaces
    </h4>
    <p class="mb-4 text-gray-700 dark:text-gray-300">
      Les questions recueillent des informations essentielles auprès de tous les
      participants, quel que soit le site choisi. Un questionnement stratégique
      vous aide à&nbsp;:
    </p>
    <ul class="mb-4 list-inside list-disc text-gray-700 dark:text-gray-300">
      <li>Collecter les coordonnées</li>
      <li>Comprendre le niveau d'expérience des participants</li>
      <li>
        Recueillir les restrictions alimentaires ou besoins d'accessibilité
      </li>
      <li>Évaluer la disponibilité et l'engagement</li>
    </ul>
    <p class="mb-2 font-semibold text-gray-700 dark:text-gray-300">
      Types de questions disponibles&nbsp;:
    </p>
    <ul class="mb-4 list-inside list-disc text-gray-700 dark:text-gray-300">
      <li>
        <span class="font-semibold">Informations personnelles</span>&nbsp;:
        Noms, adresse
      </li>
      <li>
        <span class="font-semibold">Texte court</span>&nbsp;: Numéros de
        téléphone, contacts d'urgence
      </li>
      <li>
        <span class="font-semibold">Texte long</span>&nbsp;: Descriptions
        d'expérience, demandes spéciales, détails de disponibilité
      </li>
      <li>
        <span class="font-semibold">Choix unique</span>&nbsp;: Méthode de
        communication préférée, tailles de T-shirt
      </li>
      <li>
        <span class="font-semibold">Choix multiples</span>&nbsp;: Compétences,
        expériences précédentes, centres d'intérêt
      </li>
      <li>
        <span class="font-semibold">Échelle</span>&nbsp;: Niveau d'expérience
        (1-10), heures de disponibilité
      </li>
      <li>
        <span class="font-semibold">Évaluation</span>&nbsp;: Classements de
        préférence, niveau de confort pour différentes tâches
      </li>
    </ul>
    <p class="mb-6 text-gray-700 dark:text-gray-300">
      <span class="font-semibold">Astuce&nbsp;:</span> Marquez les questions critiques
      comme obligatoires, mais gardez des questions optionnelles pour recueillir
      des informations supplémentaires qui pourraient améliorer la planification
      de votre événement.
    </p>

